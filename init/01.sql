
CREATE DATABASE IF NOT EXISTS rfefdb CHARACTER SET utf8 COLLATE utf8_general_ci;
GRANT ALL PRIVILEGES ON rfefdb.* TO 'carlosbouzas'@'%';
use rfefdb;

-- rfefdb.competicion definition

CREATE TABLE `competicion` (
                               `id` bigint(20) NOT NULL AUTO_INCREMENT,
                               `nombre` varchar(255) NOT NULL,
                               PRIMARY KEY (`id`),
                               UNIQUE KEY `UK_nombre` (`nombre`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

-- rfefdb.grupo definition

CREATE TABLE `grupo` (
                         `id` bigint(20) NOT NULL AUTO_INCREMENT,
                         `descripcion` varchar(225) NOT NULL,
                         `nombre` varchar(50) NOT NULL,
                         `competicion_id` bigint(20) NOT NULL,
                         PRIMARY KEY (`id`),
                         KEY `fk_grupo_competicion_id` (`competicion_id`),
                         CONSTRAINT `fk_grupo_competicion_id` FOREIGN KEY (`competicion_id`) REFERENCES `competicion` (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

-- rfefdb.arbitro definition

CREATE TABLE `arbitro` (
                           `id` bigint(20) NOT NULL AUTO_INCREMENT,
                           `apellidos` varchar(50) NOT NULL,
                           `federacion` varchar(50) DEFAULT NULL,
                           `nombre` varchar(50) NOT NULL,
                           `competicion_id` bigint(20) NOT NULL,
                           PRIMARY KEY (`id`),
                           KEY `fk_arbitro_competicion_id` (`competicion_id`),
                           CONSTRAINT `fk_arbitro_competicion_id` FOREIGN KEY (`competicion_id`) REFERENCES `competicion` (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;





-- rfefdb.equipo definition

CREATE TABLE `equipo` (
                          `id` bigint(20) NOT NULL AUTO_INCREMENT,
                          `ano_fundacion` int(11) NOT NULL,
                          `escudo` varchar(255) DEFAULT NULL,
                          `nombre` varchar(255) NOT NULL,
                          `nombre_corto` varchar(255) DEFAULT NULL,
                          `grupo_id` bigint(20) NOT NULL,
                          PRIMARY KEY (`id`),
                          UNIQUE KEY `UK_nombre` (`nombre`),
                          UNIQUE KEY `UK_escudo` (`escudo`),
                          UNIQUE KEY `UK_nombre_corto` (`nombre_corto`),
                          KEY `fk_equipo_grupo_id` (`grupo_id`),
                          CONSTRAINT `fk_equipo_grupo_id` FOREIGN KEY (`grupo_id`) REFERENCES `grupo` (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;


-- rfefdb.estadio definition

CREATE TABLE `estadio` (
                           `id` bigint(20) NOT NULL AUTO_INCREMENT,
                           `ano_construccion` bigint(20) DEFAULT NULL,
                           `capacidad` bigint(20) NOT NULL,
                           `nombre` varchar(50) NOT NULL,
                           `equipo_id` bigint(20) NOT NULL,
                           PRIMARY KEY (`id`),
                           UNIQUE KEY `UK_equipo_id` (`equipo_id`),
                           CONSTRAINT `fk_estadio_equipo_id` FOREIGN KEY (`equipo_id`) REFERENCES `equipo` (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;


-- rfefdb.jugador definition

CREATE TABLE `jugador` (
                           `id` bigint(20) NOT NULL AUTO_INCREMENT,
                           `dorsal` int(11) NOT NULL,
                           `nombre` varchar(50) NOT NULL,
                           `posicion` varchar(20) DEFAULT NULL,
                           `equipos_id` bigint(20) NOT NULL,
                           PRIMARY KEY (`id`),
                           KEY `fk_jugador_equipo_id` (`equipos_id`),
                           CONSTRAINT `fk_jugador_equipo_id` FOREIGN KEY (`equipos_id`) REFERENCES `equipo` (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;


-- rfefdb.partido definition

CREATE TABLE `partido` (
                           `id` bigint(20) NOT NULL AUTO_INCREMENT,
                           `fecha` date NOT NULL,
                           `gol_local` int(11) DEFAULT NULL,
                           `gol_visitante` int(11) DEFAULT NULL,
                           `num_jornada` int(11) NOT NULL,
                           `arbitro_id` bigint(20) DEFAULT NULL,
                           `equipo_local_id` bigint(20) NOT NULL,
                           `equipo_visitante_id` bigint(20) NOT NULL,
                           PRIMARY KEY (`id`),
                           KEY `fk_arbitro_id` (`arbitro_id`),
                           KEY `fk_partido_equipo_local_id` (`equipo_local_id`),
                           KEY `fk_partido_equipo_visitante_id` (`equipo_visitante_id`),
                           CONSTRAINT `fk_partido_equipo_visitante_id` FOREIGN KEY (`equipo_visitante_id`) REFERENCES `equipo` (`id`),
                           CONSTRAINT `fk_arbitro_id` FOREIGN KEY (`arbitro_id`) REFERENCES `arbitro` (`id`),
                           CONSTRAINT `fk_partido_equipo_local_id` FOREIGN KEY (`equipo_local_id`) REFERENCES `equipo` (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;


-- rfefdb.suceso definition

CREATE TABLE `suceso` (
                          `minuto` int(11) DEFAULT NULL,
                          `tipo_suceso` varchar(20) DEFAULT NULL,
                          `partido_id` bigint(20) NOT NULL,
                          `jugador_id` bigint(20) NOT NULL,
                          PRIMARY KEY (`jugador_id`,`partido_id`),
                          KEY `FK76w37osicv6e7why8lqkpadk6` (`partido_id`),
                          CONSTRAINT `fk_jugador_id` FOREIGN KEY (`jugador_id`) REFERENCES `jugador` (`id`),
                          CONSTRAINT `fk_partido_id` FOREIGN KEY (`partido_id`) REFERENCES `partido` (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;


-- rfefdb.clasificacion definition

CREATE TABLE `clasificacion` (
                                 `derrotas` int(11) DEFAULT NULL,
                                 `empates` int(11) DEFAULT NULL,
                                 `num_partidos` int(11) DEFAULT NULL,
                                 `puntos` int(11) DEFAULT NULL,
                                 `victorias` int(11) DEFAULT NULL,
                                 `equipo_id` bigint(20) NOT NULL,
                                 PRIMARY KEY (`equipo_id`),
                                 CONSTRAINT `fk_equipo_id` FOREIGN KEY (`equipo_id`) REFERENCES `equipo` (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;


